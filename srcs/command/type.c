int	is_isf(char c)
{
	return (c == ' ' || c == '\t' || c == '\n');
}

int	is_redirect(char c)
{
	return (c == '<' || c == '>');
}

int	is_quote(char c)
{
	return (c == '\'' || c == '\"');
}
